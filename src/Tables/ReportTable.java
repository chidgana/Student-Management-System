public class ReportTable {

    private final String name5;
    private final String rollno5;
    private final String regno5;
    private final String phone5;
    private final String email5;
    private final String course5;
    private final String comb5;
    private final String sem5;

    public ReportTable(String name5, String rollno5, String regno5, String phone5, String email5, String course5, String comb5, String sem5) {

        this.name5 = name5;
        this.rollno5 = rollno5;
        this.regno5 = regno5;
        this.phone5 = phone5;
        this.email5 = email5;
        this.course5 = course5;
        this.comb5 = comb5;
        this.sem5 = sem5;
    }

    public String getName5() {
        return name5;
    }

    public String getRollno5() {
        return rollno5;
    }

    public String getRegno5() {
        return regno5;
    }

    public String getPhone5() {
        return phone5;
    }

    public String getEmail5() {
        return email5;
    }

    public String getCourse5() {
        return course5;
    }

    public String getComb5() {
        return comb5;
    }

    public String getSem5() {
        return sem5;
    }

}
